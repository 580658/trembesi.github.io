<h3>Akari Furutaka</h3>
<p>Akari is one of the main protagonist in Yandere School. She has a crush on Makio Sono.</p>
<p>&nbsp;</p>

<h3>Appearance</h3>
<p>Akari is depicted as being a young, pretty Japanese school girl, with long, straight, dark-violet hair, which she keeps braided into two low-pigtails. When loose, Akari's hair falls down to her waist. Her eyes have an upwards point, with large, dark-purple irises, and heart-shaped, magenta pupils. She has rather child-like features, and a somewhat underdeveloped body when compared to her female classmates. Her default outfit is the standard school uniform, a brown sailor seifuku with a rather short skirt, a white collar, red ascot, black Mary Janes, and her classic black stockings.</p>
<p>Her other outfit/ accessory choices include; An alternate school uniform, a black and white French maid uniform, black and pink cat ears, tiger ears, different stockings, etc.</p>
<p>&nbsp;</p>

<h3>Relationships</h3>
<p>Makio Sono</p>
<p>Akari seemingly was at least somewhat familiar with Makio prior to the events of the game. After realizing that one of her classmates, Meisa, shared feelings for Makio as well, Akari began to act more like an overprotective relative towards him, feeling overwhelming rage, jealousy, and a strong urge to stop Meisa, and all other rivals, from trying to begin a relationship with Makio before she could.</p>
<p>&nbsp;</p>

<h3>Students</h3>
<p>Akari seemingly doesn't care about the fates of the students, and will get rid of them if they get in her way.</p>
<p>If a student witnesses Akari killing someone, they will run away in fear. This, however, is different with Ryochi Tanaguchi and Keita Hiyama, as they will instead respond by strangling Akari from behind.</p>
<p>&nbsp;</p>

